
# init.pyの役割

# 単に言うと、そのディレクトリを「Pythonのパッケージ（ひとかたまりの部品）」として認識させるための目印です。
# 主な役割は以下の3つです。
# 1. インポートを可能にする
# これがあることで、他のファイルから import common.utils のようにドットつなぎで呼び出せるようになります。
# ※Python 3.3以降はなくても動く（名前空間パッケージ）ようになりましたが、「ここはプログラムの部品が入っている場所だよ」と明示するために今でも置くのが一般的です。
# 2. 初期化処理を書く
# そのパッケージが読み込まれた（importされた）瞬間に実行したい処理を書けます。
# 例えば：ログの設定、環境変数の読み込み、必要なライブラリのチェックなど。
# 3. インポートをスッキリさせる
# 例えば、common/utils/date_utils.py の中に format_date という関数があるとします。通常はこう書きます。
# python
# from common.utils.date_utils import format_date
# コードは注意してご使用ください。

# もし common/utils/__init__.py に以下の一行を書いておくと……
# python
# # __init__.py の中身
# from .date_utils import format_date
# コードは注意してご使用ください。

# 呼び出し側はこれだけで済みます。
# python
# from common.utils import format_date  # ファイル名を飛ばして呼べる！
# コードは注意してご使用ください。

# これを使うと、使う側が「どのファイルに何があるか」を詳しく知らなくても良くなるので便利です。
# 最初は 「中身は空っぽでいいので、とりあえず置くおまじない」 と思っておけば大丈夫です！
